    private void SchoolOrgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SchoolOrgBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = SchoolTable.getSelectedRow();
        if (selectedRow >= 0) {
            School school = (School) SchoolTable.getValueAt(selectedRow, 0);

            for (Organization og : school.getOrganizationDirectory().getOrganizationList()) {
                if (og.getType().equals(Organization.Type.School)) {
                    org = og;
                } else {
                    JOptionPane.showMessageDialog(null, "No Such Organization.", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            }
            ManageOrganizationJPanel manageOrganizationJPanel = new ManageOrganizationJPanel(container, network, org);
            container.add("ManageOrganizationJPanel", manageOrganizationJPanel);
            CardLayout layout = (CardLayout) container.getLayout();
            layout.next(container);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row from table first.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_SchoolOrgBtnActionPerformed

