    void populateTable() {
        DefaultTableModel dtm1 = (DefaultTableModel) SchoolTable.getModel();
        dtm1.setRowCount(0);
        DefaultTableModel dtm2 = (DefaultTableModel) CharityTable.getModel();
        dtm2.setRowCount(0);
        DefaultTableModel dtm3 = (DefaultTableModel) ManufactoryTable.getModel();
        dtm3.setRowCount(0);
        DefaultTableModel dtm4 = (DefaultTableModel) StorageTable.getModel();
        dtm4.setRowCount(0);
        for (Enterprise ep : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (ep instanceof School) {
                Object[] row1 = new Object[3];
                row1[0] = ep;
                row1[1] = ep.getAddress();
                row1[2] = ep.getPhonenum();
                dtm1.addRow(row1);
            } else if (ep instanceof Charity) {
                Object[] row2 = new Object[3];
                row2[0] = ep;
                row2[1] = ep.getAddress();
                row2[2] = ep.getPhonenum();
                dtm2.addRow(row2);
            } else if (ep instanceof Manufactory) {
                Object[] row3 = new Object[3];
                row3[0] = ep;
                row3[1] = ep.getAddress();
                row3[2] = ep.getPhonenum();
                dtm3.addRow(row3);
            } else if (ep instanceof Storage) {
                Object[] row4 = new Object[3];
                row4[0] = ep;
                row4[1] = ep.getAddress();
                row4[2] = ep.getPhonenum();
                dtm4.addRow(row4);
            }
        }
        //dB4OUtil.storeSystem(network);
    }

