    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        // TODO add your handling code here:
        container.remove(this);
        CardLayout layout = (CardLayout) container.getLayout();
        Component[] components = container.getComponents();
        ManageRequestJPanel manageRequestJPanel = (ManageRequestJPanel) container.getComponent(components.length - 1);
        manageRequestJPanel.populateRequest();;
        layout.previous(container);
    }//GEN-LAST:event_backBtnActionPerformed
 public void ReqeustDetailsPopulate(WorkRequest request){
        IDTxt.setText(String.valueOf(request.getReqID()));
        ATxt.setText(String.valueOf(request.getAmount()));
        RDTxt.setText(String.valueOf(request.getRequestDate()));
        STxt.setText(request.getStatus());
        if(request.getResolveDate()!=null){
        FDTxt.setText(String.valueOf(request.getResolveDate()));}else{FDTxt.setText("");}
        
        if(request.getCharity()!=null){
        CharityTxt.setText(request.getCharity().getEmployee().getOrganization().getEnterprise().toString());
        charityemp.setText(request.getCharity().getEmployee().getName());}else{CharityTxt.setText("");charityemp.setText("");
        }
        
        if(request.getManufacture()!=null){
        ManuTxt.setText(request.getManufacture().getEmployee().getOrganization().getEnterprise().toString());
        manuemp.setText(request.getManufacture().getEmployee().getName());}else{ManuTxt.setText("");manuemp.setText("");}
        
        if(request.getStorage()!=null){
        StorageTxt.setText(request.getStorage().getEmployee().getOrganization().getEnterprise().toString());
        storageemp.setText(request.getStorage().getEmployee().getName());}else{StorageTxt.setText("");storageemp.setText("");
        }
        
        if(request.getSchool()!=null){
        SchoolTxt.setText(request.getSchool().getEmployee().getOrganization().getEnterprise().toString());
        schoolemp.setText(request.getSchool().getEmployee().getName());
        }else{SchoolTxt.setText("");schoolemp.setText("");
        }
        
        IDTxt.setEnabled(false);
        ATxt.setEnabled(false);
        RDTxt.setEnabled(false);
        STxt.setEnabled(false);
        FDTxt.setEnabled(false);
        CharityTxt.setEnabled(false);
        SchoolTxt.setEnabled(false);
        ManuTxt.setEnabled(false);
        StorageTxt.setEnabled(false);
        schoolemp.setEnabled(false);
        charityemp.setEnabled(false);
        manuemp.setEnabled(false);
        storageemp.setEnabled(false);
 }

