    public void populateManufactureTable() {
        DefaultTableModel dtm1 = (DefaultTableModel) ManufactureWorkTable.getModel();
        dtm1.setRowCount(0);
        for (WorkRequest req : network.getPool().getWorkRequestList()) {if(req.getManufactoryTodo()==null){}
        else if(req.getManufactoryTodo().getName().equals(account.getEmployee().getOrganization().getEnterprise().getName()))
            {if(req.getStatus().equals("Approved")||req.getStatus().equals("Producing")){
            Object[] row1 = new Object[3];
            row1[0] = req;
            row1[1] = req.getAmount();
            row1[2] = req.getStatus();
            dtm1.addRow(row1);}
        }}
        
        DefaultTableModel dtm2 = (DefaultTableModel) ManufactureHistoryTable.getModel();
        dtm2.setRowCount(0);
        for (WorkRequest req : network.getPool().getWorkRequestList()) {
            if(req.getManufacture()!=null){if(req.getManufacture().getEmployee().getOrganization().equals(account.getEmployee().getOrganization())){
            Object[] row2 = new Object[3];
            row2[0] = req;
            row2[1] = req.getAmount();
            row2[2] = req.getStatus();
            dtm2.addRow(row2);}}
        }
        FinishDateTxt.setEnabled(false);
        submitbtn.setEnabled(false);
        cancelbtn.setEnabled(false);
        finishBtn.setEnabled(true);
    }
