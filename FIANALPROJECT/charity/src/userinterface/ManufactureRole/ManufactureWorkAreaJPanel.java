            WorkRequest request = (WorkRequest) ManufactureHistoryTable.getValueAt(selectedRow, 0);
            ViewManuRequestJPanel viewManuRequestJPanel = new ViewManuRequestJPanel(request, container, account, network);
            container.add("ViewManuRequestJPanel", viewManuRequestJPanel);
