    private void submitbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbtnActionPerformed
        int selectedRow = SchoolWorkTable.getSelectedRow();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        if (selectedRow >= 0) {
            WorkRequest request = (WorkRequest) SchoolWorkTable.getValueAt(selectedRow, 0);
            request.setStatus("Finished");
            try {
                sdf.parse(FinishDateTxt.getText());
            } catch (Exception exc) {
                JOptionPane.showMessageDialog(this, "Please fill date in yyyy-MM-dd format into date field!!");
                return;
            }
            try {
                sdf.setLenient(false);
                sdf.parse(FinishDateTxt.getText());
            } catch (Exception exc) {
                JOptionPane.showMessageDialog(this, "Date is illegal!!!");
                return;
            }
            try {
                request.setResolveDate(sdf.parse(FinishDateTxt.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Fail!");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        FinishDateTxt.setText("");
        FinishDateTxt.setEnabled(false);
        submitbtn.setEnabled(false);
        cancelbtn.setEnabled(false);
        ReceiveBtn.setEnabled(true);
        populateSchoolTable();
    }//GEN-LAST:event_submitbtnActionPerformed

