    public void populateSchoolTable() {
        DefaultTableModel dtm1 = (DefaultTableModel) SchoolWorkTable.getModel();
        dtm1.setRowCount(0);
        for (WorkRequest req : network.getPool().getWorkRequestList()) {
            if(req.getSchool().getEmployee().getOrganization().equals(account.getEmployee().getOrganization())){
            Object[] row = new Object[3];
            row[0] = req;
            row[1] = req.getAmount();
            row[2] = req.getStatus();
            dtm1.addRow(row);}else{}
        }
        FinishDateTxt.setEnabled(false);
        submitbtn.setEnabled(false);
        cancelbtn.setEnabled(false);
    }
