    private void populate(){
         DefaultTableModel dtm = (DefaultTableModel) tblViewCustomer.getModel();
         dtm.setRowCount(0);
        
            for(Ticket t : customer.getTicketList()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = t; 
            Date date = t.getFlight().getDate();  
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");  
            String strDate = dateFormat.format(date);
            row[1] = strDate;
            row[2] = t.getFlight().getTime();
            row[3] = t.getFlight().getDeparture();
            row[4] = t.getFlight().getDestination();
            row[5] = t.getSeat();
            dtm.addRow(row);    
           
        }
    }
