    private void btnDeleteFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteFlightActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblViewCustomer.getSelectedRow();
        if(selectedRow >=0){
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would your like to delete the flight?","Warning",JOptionPane.WARNING_MESSAGE);
            if(dialogResult == JOptionPane.YES_NO_OPTION){
                Ticket t = (Ticket) tblViewCustomer.getValueAt(selectedRow,0);
                customer.deleteTicket(t);
                populate();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row.","Warning",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnDeleteFlightActionPerformed

