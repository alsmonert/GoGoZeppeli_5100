    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustomer .getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row to view details.","Warning",JOptionPane.WARNING_MESSAGE);
        }else{
            Customer customer = (Customer) tblCustomer.getValueAt(selectedRow,0);
            ViewCustomerJPanel panel = new ViewCustomerJPanel(travelAgencyCL,customer);
            travelAgencyCL.add("ViewCustomerJPanel",panel);
            CardLayout layout = (CardLayout) travelAgencyCL.getLayout();
            layout.next(travelAgencyCL);}
    }//GEN-LAST:event_btnViewDetailsActionPerformed
    
    private void btnDeleteCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCustomerActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustomer.getSelectedRow();
        if(selectedRow >=0){
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would your like to delete this?","Warning",JOptionPane.WARNING_MESSAGE);
            if(dialogResult == JOptionPane.YES_NO_OPTION){
                Customer customer = (Customer) tblCustomer.getValueAt(selectedRow,0);
                customer.deleteAll();
                customerDirectory.deleteCustomer(customer);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row from table first.","Warning",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnDeleteCustomerActionPerformed

