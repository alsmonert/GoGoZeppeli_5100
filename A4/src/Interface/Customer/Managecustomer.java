    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustomer.getSelectedRow();
        if(selectedRow >= 0){
            Customer customer = (Customer) tblCustomer.getValueAt(selectedRow,0);
            BookCustomerFlightJPanel panel = new BookCustomerFlightJPanel(travelAgencyCL,customer,fs,ad);
            travelAgencyCL.add("BookCustomerFlightJPanel",panel);
            CardLayout layout = (CardLayout) travelAgencyCL.getLayout();
            layout.next(travelAgencyCL);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnBookActionPerformed

